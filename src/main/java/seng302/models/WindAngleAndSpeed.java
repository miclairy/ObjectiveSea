public class WindAngleAndSpeed {

    double windAngle;
    double speed;

    public WindAngleAndSpeed(double windAngle, double speed) {
        this.windAngle = windAngle;
        this.speed = speed;
    }

    public double getWindAngle() {
        return windAngle;
    }

    public double getSpeed() {
        return speed;
    }



}
